let numero = Number(prompt("Digite um número de 1 até 7:  "));

if(numero == 1){
    document.write("Domingo!");
} else if(numero == 2){
    document.write("Segunda!");
} else if(numero == 3){
    document.write("Terça!");
} else if(numero == 4){
    document.write("Quarta!");
} else if(numero == 5){
    document.write("Quinta!");
} else if(numero == 6){
    document.write("Sexta!");
} else if(numero == 7){
    document.write("Sábado!");
} else {
    document.write("Número inválido!")
}